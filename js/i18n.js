const DEFAULT_LANG = 'sq';
const STORAGE_KEY = 'miranda-dent-lang';

function applyLanguage(lang) {
  const dict = translations[lang] || translations[DEFAULT_LANG];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  document.documentElement.setAttribute('lang', lang);

  document.querySelectorAll('.lang-switch button').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) { /* ignore */ }
}

function initLanguage() {
  let saved = DEFAULT_LANG;
  try { saved = localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG; } catch (e) { /* ignore */ }
  applyLanguage(saved);

  const switcher = document.querySelector('.lang-switch');

  document.querySelectorAll('.lang-switch button').forEach(btn => {
    btn.addEventListener('click', (e) => {
      const isMobile = window.matchMedia('(max-width: 640px)').matches;
      const isClosedTrigger = isMobile && btn.classList.contains('active') && !switcher.classList.contains('open');

      if (isClosedTrigger) {
        // First tap on mobile just opens the dropdown of language options
        e.stopPropagation();
        switcher.classList.add('open');
        return;
      }

      applyLanguage(btn.getAttribute('data-lang'));
      switcher.classList.remove('open');
    });
  });

  // Tapping anywhere else closes the open dropdown
  // Tapping anywhere else closes the open dropdown
  document.addEventListener('click', (e) => {
    if (switcher && !switcher.contains(e.target)) {
      switcher.classList.remove('open');
    }
  });
}

document.addEventListener('DOMContentLoaded', initLanguage);